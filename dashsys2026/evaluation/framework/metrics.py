"""
metrics.py -- Evaluation Metrics for Text-to-SQL

Computes the five core evaluation metrics from experiment results for the
VLDB research paper: "Schema-Aware Prompt Engineering for Text-to-SQL in
Analytical Databases".

Metrics:
    1. Execution Accuracy (EX):  % of queries that execute without syntax errors.
    2. Result Correctness (RC):  % producing correct output (exact/semantic match).
    3. Schema Linking Accuracy (SL): correct identification of tables and columns
       measured via F1 score.
    4. Token Efficiency (TE): prompt tokens required per query (lower is better).
    5. Latency (L): end-to-end time from query to result in milliseconds.

All computations use only the Python standard library (no pandas, numpy, etc.).
"""

from __future__ import annotations

import math
import logging
from collections import defaultdict
from dataclasses import dataclass, field
from typing import Optional

logger = logging.getLogger(__name__)

# The six benchmark query categories used throughout the evaluation.
BENCHMARK_CATEGORIES: list[str] = [
    "Simple_SELECT",
    "Aggregation",
    "Window_Functions",
    "Time_Series",
    "Complex_JOINs",
    "ClickHouse_Specific",
]


# ---------------------------------------------------------------------------
# Data classes
# ---------------------------------------------------------------------------

@dataclass
class QueryResult:
    """Captures per-query evaluation results.

    Each instance represents a single benchmark query that was sent through
    the text-to-SQL pipeline along with all signals needed to compute the
    five evaluation metrics.

    Attributes:
        query_id:           Unique identifier for the benchmark query.
        category:           One of the six benchmark categories (e.g.
                            ``"Aggregation"``, ``"Window_Functions"``).
        predicted_sql:      SQL generated by the model under evaluation.
        gold_sql:           Ground-truth SQL from the benchmark.
        executed_successfully:
                            ``True`` if ``predicted_sql`` ran without syntax
                            or runtime errors.
        result_correct:     ``True`` if the query output matched the gold
                            result (exact or semantic match).
        schema_linking_f1:  F1 score (0.0--1.0) measuring how accurately
                            the model identified the correct tables and
                            columns.
        input_tokens:       Number of prompt (input) tokens consumed.
        output_tokens:      Number of completion (output) tokens generated.
        latency_ms:         End-to-end latency in milliseconds from query
                            submission to final result.
        error:              Error message if execution or comparison failed;
                            empty string otherwise.
    """

    query_id: str
    category: str
    predicted_sql: str
    gold_sql: str
    executed_successfully: bool
    result_correct: bool
    schema_linking_f1: float
    input_tokens: int
    output_tokens: int
    latency_ms: float
    error: str = ""


@dataclass
class MetricsSummary:
    """Aggregate evaluation metrics computed over a collection of queries.

    Attributes:
        execution_accuracy:    Fraction of queries that executed without error
                               (0.0--1.0).
        result_correctness:    Fraction of queries producing correct results
                               (0.0--1.0).
        schema_linking_accuracy:
                               Mean schema-linking F1 across all queries
                               (0.0--1.0).
        avg_token_efficiency:  Mean number of input tokens per query.
        avg_latency:           Mean end-to-end latency in milliseconds.
        per_category_metrics:  Mapping from category name to a nested
                               ``MetricsSummary`` for that category.  Only
                               populated by :meth:`MetricsCalculator.compute_all`;
                               set to an empty dict in sub-summaries.
        total_queries:         Total number of queries evaluated.
        successful_queries:    Number of queries that executed without error.
        correct_queries:       Number of queries that produced correct output.
    """

    execution_accuracy: float
    result_correctness: float
    schema_linking_accuracy: float
    avg_token_efficiency: float
    avg_latency: float
    per_category_metrics: dict[str, "MetricsSummary"] = field(default_factory=dict)
    total_queries: int = 0
    successful_queries: int = 0
    correct_queries: int = 0


# ---------------------------------------------------------------------------
# MetricsCalculator
# ---------------------------------------------------------------------------

class MetricsCalculator:
    """Compute evaluation metrics from a list of :class:`QueryResult` objects.

    This is the main entry point for the evaluation pipeline.  Given a list
    of per-query results it produces an aggregate :class:`MetricsSummary`
    with optional breakdowns by benchmark category and query difficulty.

    Example::

        calc = MetricsCalculator()
        summary = calc.compute_all(results)
        print(calc.format_table(summary))
        payload = calc.to_dict(summary)

    All methods are deterministic and side-effect free.
    """

    # ------------------------------------------------------------------ #
    # Core computation
    # ------------------------------------------------------------------ #

    def compute_all(self, results: list[QueryResult]) -> MetricsSummary:
        """Compute aggregate metrics including per-category breakdowns.

        This is the primary method most callers should use.  It computes
        the five headline metrics *and* populates
        :pyattr:`MetricsSummary.per_category_metrics` with a sub-summary
        for every category present in *results*.

        Args:
            results: List of per-query evaluation results.

        Returns:
            A fully populated :class:`MetricsSummary`.
        """
        summary = self._summarize(results)

        # Build per-category sub-summaries.
        categories = sorted({r.category for r in results})
        summary.per_category_metrics = self.compute_per_category(
            results, categories
        )

        return summary

    def compute_per_category(
        self,
        results: list[QueryResult],
        categories: list[str],
    ) -> dict[str, MetricsSummary]:
        """Compute a separate :class:`MetricsSummary` for each category.

        Only categories that appear in *results* **and** in *categories*
        are included in the returned dictionary.  Categories listed in
        *categories* but absent from *results* are silently skipped.

        Args:
            results:    Full list of query results.
            categories: Category names to compute summaries for.

        Returns:
            Mapping from category name to its :class:`MetricsSummary`.
        """
        groups: dict[str, list[QueryResult]] = defaultdict(list)
        for r in results:
            groups[r.category].append(r)

        out: dict[str, MetricsSummary] = {}
        for cat in categories:
            if cat in groups:
                out[cat] = self._summarize(groups[cat])
        return out

    def compute_by_difficulty(
        self,
        results: list[QueryResult],
    ) -> dict[str, MetricsSummary]:
        """Compute metrics grouped by inferred query difficulty.

        Difficulty is derived from the benchmark category:

        * **easy**: ``Simple_SELECT``
        * **medium**: ``Aggregation``, ``Time_Series``
        * **hard**: ``Window_Functions``, ``Complex_JOINs``,
          ``ClickHouse_Specific``

        Args:
            results: Full list of query results.

        Returns:
            Mapping from difficulty label to its :class:`MetricsSummary`.
        """
        difficulty_map: dict[str, str] = {
            "Simple_SELECT": "easy",
            "Aggregation": "medium",
            "Time_Series": "medium",
            "Window_Functions": "hard",
            "Complex_JOINs": "hard",
            "ClickHouse_Specific": "hard",
        }

        groups: dict[str, list[QueryResult]] = defaultdict(list)
        for r in results:
            difficulty = difficulty_map.get(r.category, "unknown")
            groups[difficulty].append(r)

        return {
            level: self._summarize(group)
            for level, group in sorted(groups.items())
        }

    # ------------------------------------------------------------------ #
    # Serialization helpers
    # ------------------------------------------------------------------ #

    def to_dict(self, summary: MetricsSummary) -> dict:
        """Convert a :class:`MetricsSummary` to a plain dictionary.

        The returned dictionary is JSON-serializable and suitable for
        writing to experiment log files.

        Args:
            summary: The metrics summary to serialize.

        Returns:
            A nested ``dict`` mirroring the summary structure.
        """
        d: dict = {
            "execution_accuracy": summary.execution_accuracy,
            "result_correctness": summary.result_correctness,
            "schema_linking_accuracy": summary.schema_linking_accuracy,
            "avg_token_efficiency": summary.avg_token_efficiency,
            "avg_latency": summary.avg_latency,
            "total_queries": summary.total_queries,
            "successful_queries": summary.successful_queries,
            "correct_queries": summary.correct_queries,
        }

        if summary.per_category_metrics:
            d["per_category_metrics"] = {
                cat: self.to_dict(sub)
                for cat, sub in summary.per_category_metrics.items()
            }

        return d

    def format_table(self, summary: MetricsSummary) -> str:
        """Render a :class:`MetricsSummary` as an ASCII table.

        The output is designed for terminal display and research-log
        readability.  It contains a header section with the five headline
        metrics followed by an optional per-category breakdown.

        Args:
            summary: The metrics summary to format.

        Returns:
            A multi-line string containing the formatted table.
        """
        width = 72
        sep = "+" + "-" * (width - 2) + "+"

        lines: list[str] = []
        lines.append(sep)
        lines.append(
            _center("Evaluation Metrics Summary", width)
        )
        lines.append(sep)

        # Headline metrics
        lines.append(
            _row("Execution Accuracy (EX)", f"{summary.execution_accuracy:.4f}", width)
        )
        lines.append(
            _row("Result Correctness (RC)", f"{summary.result_correctness:.4f}", width)
        )
        lines.append(
            _row("Schema Linking Acc. (SL)", f"{summary.schema_linking_accuracy:.4f}", width)
        )
        lines.append(
            _row("Avg Token Efficiency (TE)", f"{summary.avg_token_efficiency:.1f} tokens", width)
        )
        lines.append(
            _row("Avg Latency (L)", f"{summary.avg_latency:.1f} ms", width)
        )
        lines.append(sep)

        # Counts
        lines.append(
            _row("Total Queries", str(summary.total_queries), width)
        )
        lines.append(
            _row("Successful Queries", str(summary.successful_queries), width)
        )
        lines.append(
            _row("Correct Queries", str(summary.correct_queries), width)
        )
        lines.append(sep)

        # Per-category breakdown
        if summary.per_category_metrics:
            lines.append(
                _center("Per-Category Breakdown", width)
            )
            lines.append(sep)

            header = (
                f"| {'Category':<28} "
                f"{'EX':>6} "
                f"{'RC':>6} "
                f"{'SL':>6} "
                f"{'TE':>8} "
                f"{'L(ms)':>8} |"
            )
            lines.append(header)
            lines.append(sep)

            for cat, sub in sorted(summary.per_category_metrics.items()):
                row = (
                    f"| {cat:<28} "
                    f"{sub.execution_accuracy:>6.3f} "
                    f"{sub.result_correctness:>6.3f} "
                    f"{sub.schema_linking_accuracy:>6.3f} "
                    f"{sub.avg_token_efficiency:>8.1f} "
                    f"{sub.avg_latency:>8.1f} |"
                )
                lines.append(row)

            lines.append(sep)

        return "\n".join(lines)

    # ------------------------------------------------------------------ #
    # Internal helpers
    # ------------------------------------------------------------------ #

    def _summarize(self, results: list[QueryResult]) -> MetricsSummary:
        """Build a :class:`MetricsSummary` from a list of results.

        This is the shared implementation behind :meth:`compute_all`,
        :meth:`compute_per_category`, and :meth:`compute_by_difficulty`.
        The returned summary has an empty ``per_category_metrics`` dict;
        callers that need category breakdowns populate it separately.

        Args:
            results: List of query results to summarize.

        Returns:
            A :class:`MetricsSummary` with headline metrics filled in.
        """
        total = len(results)

        if total == 0:
            return MetricsSummary(
                execution_accuracy=0.0,
                result_correctness=0.0,
                schema_linking_accuracy=0.0,
                avg_token_efficiency=0.0,
                avg_latency=0.0,
                per_category_metrics={},
                total_queries=0,
                successful_queries=0,
                correct_queries=0,
            )

        successful = sum(1 for r in results if r.executed_successfully)
        correct = sum(1 for r in results if r.result_correct)

        execution_accuracy = successful / total
        result_correctness = correct / total

        # Schema linking: mean F1 across all queries.
        schema_linking_accuracy = _safe_mean(
            [r.schema_linking_f1 for r in results]
        )

        # Token efficiency: mean input tokens.
        avg_token_efficiency = _safe_mean(
            [float(r.input_tokens) for r in results]
        )

        # Latency: mean end-to-end latency in ms.
        avg_latency = _safe_mean([r.latency_ms for r in results])

        return MetricsSummary(
            execution_accuracy=round(execution_accuracy, 6),
            result_correctness=round(result_correctness, 6),
            schema_linking_accuracy=round(schema_linking_accuracy, 6),
            avg_token_efficiency=round(avg_token_efficiency, 2),
            avg_latency=round(avg_latency, 2),
            per_category_metrics={},
            total_queries=total,
            successful_queries=successful,
            correct_queries=correct,
        )


# ---------------------------------------------------------------------------
# Module-level utility functions
# ---------------------------------------------------------------------------

def _safe_mean(values: list[float]) -> float:
    """Return the arithmetic mean of *values*, or 0.0 if the list is empty.

    Args:
        values: List of numeric values.

    Returns:
        The mean, or ``0.0`` for an empty list.
    """
    if not values:
        return 0.0
    return sum(values) / len(values)


def _center(text: str, width: int) -> str:
    """Center *text* inside a table row bounded by ``|`` characters.

    Args:
        text:  The string to center.
        width: Total row width including the ``|`` delimiters.

    Returns:
        A string of exactly *width* characters.
    """
    inner = width - 2  # account for leading and trailing '|'
    return "|" + text.center(inner) + "|"


def _row(label: str, value: str, width: int) -> str:
    """Format a label--value pair as a fixed-width table row.

    Args:
        label: Left-aligned label text.
        value: Right-aligned value text.
        width: Total row width including the ``|`` delimiters.

    Returns:
        A string of exactly *width* characters.
    """
    inner = width - 4  # account for '| ' and ' |'
    padding = inner - len(label) - len(value)
    if padding < 1:
        padding = 1
    return "| " + label + " " * padding + value + " |"
